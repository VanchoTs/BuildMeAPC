/**
 * Initializes language detection based on browser settings or localStorage.
 * Triggers the translation engine if Bulgarian is selected.
 */
export function initLanguageDetection() {
    const userLang = navigator.language || (navigator as any).userLanguage;
    const isBg = userLang.toLowerCase().startsWith('bg');
    
    // Check/Save language preference
    const storedLang = localStorage.getItem('appLang');
    if (!storedLang) {
        localStorage.setItem('appLang', isBg ? 'bg' : 'en');
    }

    if (localStorage.getItem('appLang') === 'bg') {
        document.documentElement.lang = 'bg';
        translateToBg();
        setupMutationObserver();
    }
}

function translateToBg() {
    const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    let node;
    while (walk.nextNode()) {
        node = walk.currentNode;
        const text = node.nodeValue?.trim();
        if (text && bgTranslations[text]) {
            node.nodeValue = node.nodeValue?.replace(text, bgTranslations[text]) || null;
        }
    }
    
    // Also translate placeholders
    document.querySelectorAll('[placeholder]').forEach(el => {
        const p = el.getAttribute('placeholder');
        if (p && bgTranslations[p]) {
            el.setAttribute('placeholder', bgTranslations[p]);
        }
    });

    // Translate elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key && bgTranslations[key]) {
            (el as HTMLElement).innerText = bgTranslations[key];
        }
    });
}

/**
 * Sets up a MutationObserver to dynamically translate new content added to the DOM.
 * This ensures that builds generated via AJAX or dynamic cards are translated automatically.
 */
function setupMutationObserver() {
    const observer = new MutationObserver((mutations) => {
        let shouldTranslate = false;
        for (const mutation of mutations) {
            if (mutation.addedNodes.length > 0) {
                shouldTranslate = true;
                break;
            }
        }
        if (shouldTranslate) {
            // Disconnect to avoid infinite loop when the translation itself modifies the DOM
            observer.disconnect();
            translateToBg();
            observer.observe(document.body, { childList: true, subtree: true });
        }
    });
    
    observer.observe(document.body, { childList: true, subtree: true });
}

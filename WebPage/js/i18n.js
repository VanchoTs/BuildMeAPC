let currentLang = 'en';

export function initLanguageDetection() {
    const userLang = navigator.language || navigator.userLanguage;
    const isBg = userLang.toLowerCase().startsWith('bg');
    
    // Check/Save language preference
    let storedLang = localStorage.getItem('appLang');
    if (!storedLang) {
        storedLang = isBg ? 'bg' : 'en';
        localStorage.setItem('appLang', storedLang);
    }

    currentLang = storedLang;

    if (currentLang === 'bg') {
        document.documentElement.lang = 'bg';
        
        // Translate document title
        const titleMatch = document.title.split(' - ');
        if (titleMatch[0] && bgTranslations[titleMatch[0]]) {
            document.title = bgTranslations[titleMatch[0]] + ' - BuildMeAPC';
        } else if (bgTranslations[document.title]) {
            document.title = bgTranslations[document.title];
        }

        // Wrap native dialogs
        const originalAlert = window.alert;
        window.alert = function(msg) {
            originalAlert(translateString(msg));
        };
        
        const originalConfirm = window.confirm;
        window.confirm = function(msg) {
            return originalConfirm(translateString(msg));
        };
        
        const originalPrompt = window.prompt;
        window.prompt = function(msg, def) {
            return originalPrompt(translateString(msg), def);
        };

        applyTranslations();
        
        const observer = new MutationObserver((mutations) => {
            for (const mutation of mutations) {
                mutation.addedNodes.forEach(node => {
                    if (node.nodeType === Node.ELEMENT_NODE) {
                        applyTranslations(node);
                    } else if (node.nodeType === Node.TEXT_NODE) {
                        translateTextNode(node);
                    }
                });
            }
        });
        
        observer.observe(document.body, { childList: true, subtree: true });
    }
}

function translateString(str) {
    if (!str || currentLang !== 'bg') return str;
    
    const trimmed = str.trim();
    if (bgTranslations[trimmed]) {
        return str.replace(trimmed, bgTranslations[trimmed]);
    }
    
    // Check for prefixes (e.g., "Login failed: ...")
    const keys = Object.keys(bgTranslations).sort((a, b) => b.length - a.length);
    for (const key of keys) {
        if (key.length > 3 && str.includes(key)) {
            str = str.split(key).join(bgTranslations[key]);
        }
    }
    return str;
}

function translateTextNode(node) {
    const originalText = node.nodeValue;
    const translated = translateString(originalText);
    if (originalText !== translated) {
        node.nodeValue = translated;
    }
}

function applyTranslations(root = document.body) {
    // Walk text nodes
    const walk = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null);
    let node;
    while (walk.nextNode()) {
        translateTextNode(walk.currentNode);
    }
    
    // Also translate placeholders
    const placeholders = root.querySelectorAll ? root.querySelectorAll('[placeholder]') : [];
    placeholders.forEach(el => {
        const p = el.getAttribute('placeholder');
        const translated = translateString(p);
        if (p !== translated) {
            el.setAttribute('placeholder', translated);
        }
    });

    // Translate elements with data-i18n attribute
    const dataI18n = root.querySelectorAll ? root.querySelectorAll('[data-i18n]') : [];
    dataI18n.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (bgTranslations[key]) {
            el.innerText = bgTranslations[key];
        }
    });
}
